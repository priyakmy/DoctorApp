public class FollowUpModel {
    private String followupUnit;
    private String followupId;
    private String followupNumber;

    public String getFollowupUnit() {
        return followupUnit;
    }

    public void setFollowupUnit(String followupUnit) {
        this.followupUnit = followupUnit;
    }

    public String getFollowupId() {
        return followupId;
    }

    public void setFollowupId(String followupId) {
        this.followupId = followupId;
    }

    public String getFollowupNumber() {
        return followupNumber;
    }

    public void setFollowupNumber(String followupNumber) {
        this.followupNumber = followupNumber;
    }
}
